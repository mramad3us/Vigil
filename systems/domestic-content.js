// --- Domestic Intel Value Generation ---
// Picks from domestic pools with placeholder substitution.
// Falls back to generateIntelValue() for non-domestic keys.

function generateDomesticIntelValue(fieldKey, location, orgName) {
  var pool = DOMESTIC_INTEL_VALUE_POOLS[fieldKey];
  if (!pool || pool.length === 0) {
    return generateIntelValue(fieldKey, location, orgName);
  }

  var val = pick(pool);
  if (location) {
    val = val.replace(/\{city\}/g, location.city || '?');
    val = val.replace(/\{country\}/g, location.country || '?');
  }
  val = val.replace(/\{orgName\}/g, orgName || 'unknown organization');
  return val;
}

