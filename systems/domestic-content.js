// --- Domestic Location Generation ---
// Filters THEATERS.NORTH_AMERICA.cities for US-only locations.

function generateDomesticLocation() {
  var theater = THEATERS.NORTH_AMERICA;
  var usCities = [];
  for (var i = 0; i < theater.cities.length; i++) {
    if (theater.cities[i].country === 'United States') {
      usCities.push(theater.cities[i]);
    }
  }
  var city = pick(usCities);
  return {
    theater: theater,
    theaterId: theater.id,
    city: city.city,
    country: city.country,
    lat: city.lat,
    lon: city.lon,
  };
}

